async function loadBankFromUrl(url, format) {
  if (!url) return null;
  const response = await fetch(url, { cache: "no-store" });
  if (!response.ok) {
    throw new Error("ËØçÂ∫ìÂà∑Êñ∞Â§±Ë¥•ÔºåËØ∑Ê£ÄÊü• URL ÊòØÂê¶ÂèØÁî®„ÄÇ");
  }
  const rawText = await response.text();

  // Â§ÑÁêÜ JSON Ê†ºÂºè
  try {
    const data = JSON.parse(rawText);
    if (!Array.isArray(data)) {
      throw new Error("ËØçÂ∫ìÊ†ºÂºèÂ∫î‰∏∫Êï∞ÁªÑÔºö[{ar, zh}]");
    }
    return data
      .filter((item) => item && item.ar)
      .map((item) => ({ ar: item.ar, zh: item.zh || "" }));
  } catch {
    // Èùû JSONÔºåÊåâÊñáÊú¨Â§ÑÁêÜ
  }

  const lines = rawText
    .split(/\r?\n/)
    .map((line) => line.trim())
    .filter(Boolean);

  // Â§ÑÁêÜËØçÈ¢ëÊ†ºÂºè: "word frequency" Êàñ "word\tfrequency"
  if (format === "frequency") {
    return lines.map((line) => {
      const parts = line.split(/[\s\t]+/);
      const word = parts[0];
      return { ar: word, zh: "" };
    }).filter(item => item.ar && /[\u0600-\u06FF]/.test(item.ar)); // Âè™‰øùÁïôÂåÖÂê´ÈòøÊãâ‰ºØÂ≠óÁ¨¶ÁöÑËØç
  }

  // ÊôÆÈÄöÊñáÊú¨Ê†ºÂºèÔºåÊØèË°å‰∏Ä‰∏™ËØç
  return lines
    .map((ar) => ({ ar, zh: "" }))
    .filter(item => item.ar && /[\u0600-\u06FF]/.test(item.ar));
}

