async function ensureTranslation(word) {
  if (word.zh) return word.zh;
  const cache = getTranslationCache();
  if (cache[word.ar]) {
    word.zh = cache[word.ar];
    return word.zh;
  }
  setStatus("Ê≠£Âú®ÁøªËØë...");
  const translation = await translateToChinese(word.ar);
  if (translation) {
    word.zh = translation;
    cache[word.ar] = translation;
    setTranslationCache(cache);
    updateCurrentWord();
  }
  return translation;
}

